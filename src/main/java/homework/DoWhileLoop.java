package homework;

public class DoWhileLoop {
   public static void main(String[] args) {
// j = 1, add + 1 to J until equal to 5 & print name "Stephon Mills"
      int j = 1;
      do {
         System.out.println(j);
         j++;
      } while(j <= 5);
      System.out.println("Stephon Mills");

   }
}